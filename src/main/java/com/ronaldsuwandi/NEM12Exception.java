package com.ronaldsuwandi;

public class NEM12Exception extends RuntimeException {

    public NEM12Exception() {
        super();
    }

    public NEM12Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public NEM12Exception(Throwable cause) {
        super(cause);
    }

    public NEM12Exception(String message) {
        super(message);
    }
}
